public class _float {
	public static void main(String[] args) {
		//float修饰的值必须加F或f后缀
		float a  = 1.1f;
		float b  = 2.2F;
		
		//double修饰的值可以加D或d作为后缀，也可以不加！！！！！！因为double是浮点数的默认类型
		double c = 2.3d;
		double d = 2.3D;
		double e = 2.3;
		
	}

}
