public class _zizeng {

	public static void main(String[] args) {
		int a = 1;
		System.out.println(a++);	//1;先输出结果，后做自增运算
		System.out.println(a); 		//2
		
		int b = 1;
		System.out.println(++b);	//2;先自增，再输出结果
		System.out.println(b);		//2
	}

}
